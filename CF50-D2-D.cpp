const int N = 100+5;

int EPS;
int n, k;
int x[N], y[N];
bool vis[N][N];
double memo[N][N];

double f(double d, double r){
	if(d <= r) return 1.0;
	return exp(1 - (d * d) / (r * r));
}

double DP(int pos, int left, double r){
	if(pos == n+1){
		return left == 0;
	}
	if(vis[pos][left]) return memo[pos][left];
	double d = hypot(x[pos] - x[0], y[pos] - y[0]);
	double ans = (1.0 - f(d, r)) * DP(pos + 1, left, r) + f(d, r) * DP(pos + 1, max(0, left - 1), r);
	vis[pos][left] = true;
	return memo[pos][left] = ans;
}

double P(double R){
	memset(vis, 0, sizeof vis);
	return 1.0 - DP(1,k,R);
}

int main(){
	ri(n);
	ri2(k, EPS);
	for(int i=0; i<=n; i++){
		ri2(x[i], y[i]);
	}
	double lo = 0.0, hi = 4000.0;
	for(int i=0; i<100; i++){
		double mi = (lo + hi) / 2.0;
		if(P(mi) * 1000 < EPS) hi = mi;
		else lo = mi;
	}
	printf("%.10lf\n",lo);
	return 0;
}
