/*
	Author: Racso Galvan

	Idea:

	- DP problem.

	- Just sort all the elements by increasing position and then apply DP to

	  compute the m√°ximum continuous length that ends in p[i] (falls to the right) 
	  or p[i] - h[i] (falls to the left).

	- Complexity: O(nlogn) per testcase.
*/

const int N = 800000+5;

int n;
int p[N];
int h[N];

int main(){
	int t;
	ri(t);
	for(int caso = 1; caso <= t; caso++){
		ri(n);
		for(int i = 0; i < n; i++){
			ri2(p[i], h[i]);
		}
		vector<int> perm(n);
		iota(all(perm), 0);
		sort(all(perm), [&] (int i, int j){
			return p[i] < p[j];
		});
		ll ans = 0LL;
		map<int, ll> memo;
		for(int i : perm){
			ll new_pi = max(memo[p[i]], memo[p[i] - h[i]] + h[i]);
			ll new_phi = max(memo[p[i] + h[i]], memo[p[i]] + h[i]);
			memo[p[i]] = new_pi;
			memo[p[i] + h[i]] = new_phi;
			ans = max({ans, memo[p[i]], memo[p[i] + h[i]]});
		}
		printf("Case #%d: %lld\n", caso, ans);
	}
	return 0;
}
